<h1>Ajuda do Editor de textos</h1>
<p>Olá, abaixo algumas informações importantes ao utilizar o editor de textos:</p>

<h2>Formatação de texto</h2>
<ul>
	<li>Para colar textos do Word ou outro editor de textos, use preferencialmente o ícone <span class="ico" style="background-image:url(http://localhost/emptyrepository/_assets/js/ckeditor/plugins/icons.png?t=F0RD);background-position:0 -1584px;background-size:auto;"></span> para colar o texto limpando as formatações existentes que podem quebrar o layout do site.</li>
	<li>Removendo a formatação: utilize o ícone <span class="ico" style="background: url(http://localhost/emptyrepository/_assets/js/ckeditor/plugins/icons.png?t=F0RD);background-position:0 -1680px;background-size:auto;"></span> para remover qualquer formatação existente no texto.</li>
	<li>Tamanho de fonte, tipo de fonte e outras configurações, são feitas diretamente no site. Caso deseje algo diferente no site, entre em contato com a Deen.</li>
	<li>No menu "Formatação", você pode definir se o texto é um título, ou um texto normal.</li>
	<li>Quebra de linha: para ter uma quebra de linha simples, utilize <img src="<?php echo site_url(IMG_MANAGER.'key_shift.png');?>" />+<img src="<?php echo site_url(IMG_MANAGER.'key_return.png');?>" /> (shift+enter). Para quebra de linha dupla, utilize somente <img src="<?php echo site_url(IMG_MANAGER.'key_return.png');?>" /> (enter).</li>
</ul>

<h2>Links e outros objetos</h2>
<ul>
	<li>Para colocar links para outras partes do site ou para outros sites, utilize o ícone <span class="ico" style="background-image:url(http://localhost/emptyrepository/_assets/js/ckeditor/plugins/icons.png?t=F0RD);background-position:0 -1248px;background-size:auto;"></span>.</li>
	<li>Você ainda tem a opção de adicionar imagens (<span class="ico" style="background-image:url(http://localhost/emptyrepository/_assets/js/ckeditor/plugins/icons.png?t=F0RD);background-position:0 -936px;background-size:auto;"></span>) e tabelas (<span class="ico" style="background-image:url(http://localhost/emptyrepository/_assets/js/ckeditor/plugins/icons.png?t=F0RD);background-position:0 -1896px;background-size:auto;"></span>) diretamente no texto. Para quaisquer outros itens, entre em contato conosco que encontraremos a melhor forma de adicionar o conteúdo ao seu site.</li>
